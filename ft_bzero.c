#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*pointer;

	i = 0;
	pointer = (char *)s;
	while (i < n)
	{
	pointer[i] = '\0';
	i++;
	}
}
