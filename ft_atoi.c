#include "libft.h"

int	ft_atoi(const char *s)
{
	unsigned int	num;
	int				i;
	int				np;

	np = 1;
	i = 0;
	num = 0;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\f'
		|| s[i] == '\r' || s[i] == '\n' || s[i] == '\v')
		i++;
	if (s[i] == '+' || s[i] == '-')
		if (s[i++] == '-')
			np = -1;
	while (s[i] >= '0' && s[i] <= '9')
	{
		num = num * 10 + (s[i] - '0');
		if (num > 2147483647 && np == 1)
			return (-1);
		if (num > 2147483648 && np == -1)
			return (0);
		i++;
	}
	return ((int)(np * num));
}
